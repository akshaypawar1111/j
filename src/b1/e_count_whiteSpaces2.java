package b1;

public class e_count_whiteSpaces2 
{
public static void main(String[] args) {
	
	
	
	
	String a="akshay pawa  r";
	
	
	int c=0;
	
	
	for(int i=0; i<=a.length()-1; i++)
	{
		
		char vv = a.charAt(i);
		
		if(vv==' ')
		{
			
			c++;
		}
		
		
	}
	
	
	System.out.println(c);
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
