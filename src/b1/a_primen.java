public class a_primen 
{
public static void main(String[] args) 
{

	
	
	
	int a=9;
	int b=0;
	
	
	for(int i=2; i<a; i++)
	{
		
		if(a%i==0)
		{
			
			b++;
			break;
			
		}
		
		if(b==1)
		{
			
			System.out.println("number is prime");
		}
		else
		{
			
			System.out.println("number is not Prime");
		}
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
