package p1;

import java.util.Scanner;

public class odd_even
{
public static void main(String[] args) 
{

	
	
	
	Scanner s=new Scanner(System.in);
	
	int num = s.nextInt();
	
	
	
	if(num%2==1)
	{
		
		
		System.out.println("given number is odd");
	}
	
	

	else if(num%2==0)
	{
		
		
		System.out.println("given number is even");
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
